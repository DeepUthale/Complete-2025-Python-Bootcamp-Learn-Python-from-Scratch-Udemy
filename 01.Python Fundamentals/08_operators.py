no1 = 34

no2 = 2

# Arithmetic Operators
print("Number1 + Number2 = ", no1 + no2)
print("Number1 - Number2 = ", no1 - no2)
print("Number1 * Number2 = ", no1 * no2)
print("Number1 / Number2 = ", no1 / no2)
print("Number1 % Number2 = ", no1 % no2)
print("Number1 // Number2 = ", no1 // no2)
print("Number1 ** Number2 = ", no1 ** no2)

# Conditional Operators
print(no1 >= 4)
print(no1 <= 4)
print(no1 == 34) # Is a equal to 34?
print(no1 != 34) # Is a not-equal to 4?

# Logical Operators
print("For And Operator...")
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print("For Or Operator...")
print(True or True)
print(True or False)
print(False or True)
print(False or False)

print("For Not Operator...")
print(not(True))
print(not(False))

# Assignment Operators
no1 = 32
print(no1)
no1 += 3
print(no1)

no2 = 2
print(no2)
no2 *= 3
print(no2)
