# Indexing
name = "DeepU"
print(name[0 : 2]) # Goes from 0 to 2-1 i.e. 0 to 1
print(name[2 : -1]) # Same as name[2 : 3]

# Slicing
name = "DeepU0123456"
# print(name[0:10:n]) # Skip n - 1 characters
print(name[0:10:1]) # Skip 0 characters
print(name[0:10:3]) # Skip 3 - 1 i.e. 2 characters

print(name[:4]) # Replace the first empty number with 0 # name[0:4]
print(name[1:]) # Replace the second empty number with lenght # name[1:15]
